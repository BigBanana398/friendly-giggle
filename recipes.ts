import { Recipe, Ingredient, RecipeCategory } from './types';

const mapCategory = (rawCat: string): RecipeCategory => {
    if (rawCat.includes('肉')) return '肉类';
    if (rawCat.includes('海鲜') || rawCat.includes('鱼') || rawCat.includes('虾')) return '海鲜';
    if (rawCat.includes('蔬菜') || rawCat.includes('素')) return '蔬菜';
    if (rawCat.includes('菌')) return '菌类';
    if (rawCat.includes('汤')) return '汤品';
    if (rawCat.includes('主食') || rawCat.includes('饭') || rawCat.includes('面') || rawCat.includes('粉')) return '主食';
    return '其他';
};

const parseIngredient = (str: string): Ingredient => {
    const parts = str.split(' ');
    const name = parts[0];
    const rest = parts.slice(1).join(' ');
    let amount = '适量';
    let unit = '';
    
    if (rest) {
        const match = rest.match(/^([\d\./]+)(.*)$/);
        if (match) {
            amount = match[1];
            unit = match[2] || '';
        } else {
            amount = rest;
        }
    }

    let category: Ingredient['category'] = 'other';
    if (/肉|鸡|鸭|牛|羊|排骨|培根|火腿/.test(name)) category = 'meat';
    else if (/鱼|虾|蟹|贝|鱿鱼|海鲜/.test(name)) category = 'seafood';
    else if (/菜|葱|姜|蒜|椒|茄|豆|菇|笋|瓜|卜|薯|茄子|番茄/.test(name)) category = 'produce';
    else if (/奶|蛋|油|黄油|芝士/.test(name)) category = 'dairy';
    else if (/酱|油|盐|糖|醋|酒|粉|米|面/.test(name)) category = 'pantry';

    return { name, amount, unit, category };
};

